package Casting;

public class implicit_casting 
{
	public static void main(String[] args)
	{
		int a=33;				// size= 4 byte
		System.out.println(a);
		
		// implicit casting is used for  increase size of datatype 
		
		double b=a; 			// size= 8 byte
		System.out.println(b);
		
		
	}

}
